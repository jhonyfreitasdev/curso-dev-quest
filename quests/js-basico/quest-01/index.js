function somar(numero1, numero2) {
    let soma = numero1 + numero2;
    return 'A soma dos dois números é: ' + soma;
}
console.log(somar(7, 9))


function subtrair(numero1, numero2){
    let subtracao = numero1 - numero2;
    return 'A subtração dos dois números é: ' + subtracao;
}
console.log(subtrair(50, 20))


function dividir(numero1, numero2){
    let divisao = numero1 / numero2;
    return 'A divisão dos dois número é: ' + divisao; 
}
console.log(dividir(30, 2))


function multiplicar(numero1, numero2){
    let multiplicacao = numero1 * numero2;
    return 'A multiplicação dos dois números é: ' + multiplicacao;
}
console.log(multiplicar(9, 6))